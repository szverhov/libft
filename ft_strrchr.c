#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*res;

	res = 0;
	i = -1;
	while (s[++i])
		if (s[i] == c)
			res = (char*)s + i;
	if (s[i] == c)
		res = (char*)s + i;
	return (res);
}
