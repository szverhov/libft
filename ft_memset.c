#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	write;
	unsigned char	*res;

	res = (unsigned char*)b;
	write = c;
	i = 0;
	while (i < len)
	{
		res[i] = write;
		i++;
	}
	return (b);
}
