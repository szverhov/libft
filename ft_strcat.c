#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *res;

	res = s1;
	while (*res != 0)
		res++;
	while (*s2 != 0)
	{
		*res = *s2;
		res++;
		s2++;
	}
	*res = '\0';
	return (s1);
}
